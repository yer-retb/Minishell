#include "libft.h"

char	*ft_strnstr(const char *str, const char *needle, size_t len)
{
	unsigned long	i;
	int				j;

	i = 0;
	while (needle[i] == '\0')
		return ((char *)str);
	while ((str[i]) && (i < len))
	{
		j = 0;
		while (str[i + j] == needle[j] && i + j < len)
		{
			j++;
			if (needle[j] == '\0')
				return ((char *)&str[i]);
		}
		i++;
	}
	return (0);
}
